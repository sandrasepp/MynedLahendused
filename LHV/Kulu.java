package LHV;

public class Kulu {
    String nimetus;
    double summa;

    public Kulu(String nimetus, double summa) {
        this.nimetus = nimetus;
        this.summa = summa;
    }

    public String getNimetus() {
        return nimetus;
    }

    public double getSumma() {
        return summa;
    }
}